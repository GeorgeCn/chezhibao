    }
    //拼音首个字母
    public function getFirstPY($chinese)
    {
        if ($chinese === "重庆市") {
            return "cqs";
        }
        $chinese = mb_convert_encoding($chinese, "GB18030");
        $result  = '';
        for ($i = 0; $i < strlen($chinese); $i++) {
            $p = ord(substr($chinese, $i, 1));
            if ($p > 160) {
                $q = ord(substr($chinese, ++$i, 1));
                $p = $p * 256 + $q - 65536;
            }
            $result .= substr($this->zhToPy($p), 0, 1);
        }
        return $result;
    }

    //-------------------中文转拼音--------------------------------//
    private function zhToPy($num, $blank = '-')
    {
        if ($num > 0 && $num < 160) {
            return chr($num);
        } elseif ($num < -20319 || $num > -10247) {
            return $blank;
        } else {
            foreach ($this->pylist as $py => $code) {
                if ($code > $num) {
                    break;
                }

                $result = $py;
            }
            return $result;
        }
    }

    private function zhToPys($chinese, $delimiter = ' ', $first = 0)
    {
        $result = array();
        for ($i = 0; $i < strlen($chinese); $i++) {
            $p = ord(substr($chinese, $i, 1));
            if ($p > 160) {
                $q = ord(substr($chinese, ++$i, 1));
                $p = $p * 256 + $q - 65536;
            }
            $result[] = $this->zhToPy($p);
            if ($first) {
                return $result[0];
            }
        }
        return implode($delimiter, $result);
    }
